/*
 * leftBitCount - returns count of number of consective 1's in
 *     left-hand (most significant) end of word.
 *   Examples: leftBitCount(-1) = 32, leftBitCount(0xFFF0F0F0) = 12
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 50
 *   Rating: 4
 */
int leftBitCount(int x) {
	int temp = x;
	int r;
	int s;
	int mask = !(~x);
	r = !(~(temp>>16))<<4;
	temp = temp << r;
	s = (!(~temp>>24))<<3;
	temp = temp << s;
	r = r|s;
	s = !(~(temp>>28))<<2;
	temp = temp<<s;
	r = r|s;
	s = !(~(temp>>30))<<1;
	temp = temp<<s;
	r = r|s;
	r = r^(1&(temp>>31));

	return r + mask;
}
