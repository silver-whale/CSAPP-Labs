/* 
 * float_f2i - Return bit-level equivalent of expression (int) f
 *   for floating point argument f.
 *   Argument is passed as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point value.
 *   Anything out of range (including NaN and infinity) should return
 *   0x80000000u.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
int float_f2i(unsigned uf) {
	int exp = (uf>>23) & 0xFF;
	int frac = uf&0x7FFFFF;
	int e= exp-127;

	if(exp==0x7F800000)
		return 0x80000000u;
	if(!exp)
		return 0;
	if(e<0)
		return 0;
	if(e>30)
		return 0x80000000u;

	frac = frac|0x800000;
	if(e>=23)
		frac=frac<<(e-23);
	else
		frac=frac>>(23-e);
	if((uf>>31)&1)
		return ~frac+1;
	return frac;
}
/* 
 * float_half - Return bit-level equivalent of expression 0.5*f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_half(unsigned uf) {
	unsigned sign = uf&0x80000000;
	unsigned exp = uf & 0x7F800000;
	unsigned frac = uf & 0x007FFFFF;

	if(exp==0x7F800000) return uf;
	if((!exp)||(exp==0x00800000)){
		frac = frac | exp;
		frac = (uf&0x00FFFFFF)>>1;
		frac += ((uf&3)>>1)&(uf&1);
		return sign|frac;
	}
	return sign|((exp-1)&0x7F800000)|frac;
}
/* 
 * float_i2f - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_i2f(int x) {
	int exp = 158;
	int mask = 1<<31;
	int sign = x&mask;
	int frac;
	if(x==mask)
		return mask|(exp<<23);
	if(!x) return 0;
	if(sign)
		x = ~x+1;
	while(!(x&mask)){
		x = x<<1;
		exp = exp-1;
	}
	frac = (x&(~mask)) >> 8;
	if(x&0x80&&((x&0x7F)>0||frac&1))
		frac = frac + 1;
	return sign + (exp<<23) + frac;
}
/* 
 * float_neg - Return bit-level equivalent of expression -f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_neg(unsigned uf) {
	int nonan = 0x000000FF << 23;
	int frac = 0x7FFFFF & uf;

	if((nonan & uf) == nonan && frac)
		return uf;
	return uf ^ (1<<31);
}
/* 
 * float_twice - Return bit-level equivalent of expression 2*f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_twice(unsigned uf) {
	int exp = (uf>>23)&0xFF;
	if(!exp){
		exp = 0xFF;
		uf = (uf&0x80000000)|(uf<<1);
	}
	if(!(exp^0xFE)){
		exp=0xFF;
		uf=(uf&0x80000000)|(exp<<23);
	}
	if(exp^0xFF) uf=uf+(1<<23);
	return uf;
}
